let a=20;
let b=10;
let Operation ="Mul";
switch(Operation){
    case "Add":
        console.log(a+b);
        break;
    case "Sub":
        console.log(a-b); 
        break;
    case "Mul":
        console.log(a*b); 
        break; 
    case "Div":
        console.log(a/b);
        break;
    case "Mod":
        console.log(a%b);   
        break;
}