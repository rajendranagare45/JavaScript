//Arithmetic Op
let a=50;
let b=20;
let sum=a+b;
console.log(sum);
let sub=a-b;
console.log(sub);
let mul=a*b;
console.log(mul);
let div=a/b;
console.log(div);
let mod=a%b;
console.log(mod);

// left shift operator
let d=8; //binary code 1000
let e=d<<3;
console.log(e);

//right shift

let s=25;
let k=s>>3;
console.log(k);


//Logical
let x=12;
let y=6;
console.log(x<11 && y>0);
console.log(x<11 || y>0);

